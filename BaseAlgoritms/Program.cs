        public static string test24(string str,int n)
        {
            var result = string.Empty;
            var frontofString = 3;

            if(frontofString > str.Length)
            {
                frontofString = str.Length;
            }

            var front = str.Substring(0, frontofString);
            for (int i = 0; i < n; i++)
            {
                result += front;
            }

            return result;
        }
